    public static int getBaris(int lengthData) {

        int counter = 0;
        int nilai=1;
        while(counter != lengthData) {
            for (int j=0; j<nilai*2; j++) {
                counter++;
                if (counter == lengthData) {
                    break;
                }
                
            }
            nilai++;
        }        
        return nilai-1;

    }

